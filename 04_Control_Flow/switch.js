const mon = 3
switch (mon){
    case 1:
        console.log("Jan")
        break;
    case 2:
        console.log("Feb")
        break
    case 3:
        console.log("march")
        break
    case 4:
        console.log("April")
        break
    default:
        console.log("Default case is matched")
        break

}